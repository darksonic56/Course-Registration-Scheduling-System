    private void importButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importButtonActionPerformed
        FileInputStream fileout; // defines variable FileInputStream
        ObjectInputStream object_out = null; // defines it as null 
          try
          {
              fileout = new FileInputStream("Data.data");
              object_out = new ObjectInputStream(fileout);
          } catch(FileNotFoundException e)
          {
              System.err.println(e);
          }
          catch(IOException e)
          {
              System.err.println(e);
          }
          try
          {
              // reads in each of the static ArrayList variables from the file
              students = (ArrayList<Student>) object_out.readObject(); 
              rooms = (ArrayList<Room>) object_out.readObject();
              courses = (ArrayList<Course>) object_out.readObject();
          } 
          
          catch(Exception e)
          {
              System.err.println(e);
          }
    }//GEN-LAST:event_importButtonActionPerformed

