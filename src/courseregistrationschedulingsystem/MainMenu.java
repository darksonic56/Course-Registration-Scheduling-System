    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
      try
      {
        
          FileOutputStream fileout; // defines variable fileoutputstream
          ObjectOutputStream object_out = null; // sets the object_out to null
          try
          {
              fileout = new FileOutputStream("Data.data"); // retrives the "Data.data" into a file output stream
              object_out = new ObjectOutputStream(fileout);
          } catch(FileNotFoundException e)
          {
              System.err.println(e); // prints out an error if something goes wrong
          }
          catch(IOException e)
          {
              System.err.println(e);
          }
          try
          {
              // writes out to a file for each of the static ArrayList variables defined above 
              object_out.writeObject(students);
              object_out.writeObject(rooms);
              object_out.writeObject(courses);
          } catch(IOException e)
          {
              System.err.println(e);
          }
          object_out.close(); // closes the file after it has been saved in 
      }catch(IOException e)
      {
          System.err.println(e);
      }
        
    }//GEN-LAST:event_saveButtonActionPerformed

